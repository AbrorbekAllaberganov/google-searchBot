package newsJson;

public class RightsDetail{
	private Object language;
	private String type;
	private String value;
	private String base;

	public Object getLanguage(){
		return language;
	}

	public String getType(){
		return type;
	}

	public String getValue(){
		return value;
	}

	public String getBase(){
		return base;
	}
}
